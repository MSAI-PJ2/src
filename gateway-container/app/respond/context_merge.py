def recent_user_texts(turns: list[dict[str, Any]]) -> list[str]:
    """병합 맥락으로 쓸 최근 사용자 발화들(과거→현재 순).

    제외 대상:
    - assistant 턴 (사용자 발화만 병합)
    - STT/OCR 실패 등 텍스트 없는 기록 (event 가 있고 text 가 빈 턴)
    - 안전검사에 차단된 턴 (위기 발화를 다음 턴 분류 맥락에 섞지 않는다)
    """
    return [t.get("text", "") for t in turns
            if t.get("role") == "user" and (t.get("text") or "").strip()
            and t.get("safety") != "blocked"]


def trailing_insufficient(turns: list[dict[str, Any]]) -> int:
    """직전에서부터 연속으로 '불충분'이었던 사용자 턴 수 (완화 사다리 단계 계산용).

    - assistant 턴은 건너뛴다 (사용자 발화의 연쇄만 본다).
    - '불충분'이 아닌 사용자 턴이 나오면 그 자리에서 멈춘다 (연쇄가 끊긴 것).
    - 차단(blocked)된 턴도 연쇄를 끊는다 — 위기 개입 직후에 질문 완화 단계가
      이어져 있던 것처럼 점프하지 않도록 보수적으로 리셋.
    - 텍스트 없는 사용자 이벤트 턴(STT/OCR 실패 등)도 연쇄를 끊는다 — 그건 발화
      회피가 아니라 "말하려고 시도했는데 실패한" 기록이다. 건너뛰면 입력 실패
      구간 너머의 옛 불충분까지 이어 세서 수용 모드로 잘못 점프한다 (리뷰 지적).
    """
    count = 0
    for t in reversed(turns):
        if t.get("role") != "user":
            continue
        if not (t.get("text") or "").strip():
            break
        if t.get("safety") == "blocked" or t.get("primary") != "불충분":
            break
        count += 1
    return count
