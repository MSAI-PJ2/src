# 동사/형용사 활용어미 — 이걸 떼야 "있었어요·그랬어요" 같은 활용형이 내용어로
# 오인되지 않는다 (실험 1차에서 이어말하기의 66%가 이 부류로 오검출됐던 원인).
_ENDINGS = ("더라고요", "거잖아요", "잖아요", "겠어요", "었어요", "았어요", "랬어요",
            "겼어요", "왔어요", "봤어요", "샀어요", "났어요", "갔어요", "했어요", "됐어요",
            "습니다", "라고요", "거고요", "는데요", "은데요", "이에요", "인데요",
            "하네요", "네요", "데요", "어요", "아요", "에요", "예요", "해요", "고요",
            "겠죠", "거죠", "이죠", "하죠", "니까", "는데", "은데", "면서", "지만", "죠", "다")
# 범용어: 어떤 화제에서도 나올 수 있어 "새 내용어"로 치지 않는 말 + 폐쇄류 부사
# + 흔한 수긍·존재 표현("네네"과 "있었어요·됐어요"의 어간). 주의: '괜찮'은 넣지
# 않는다 — "이제 괜찮아요"는 상태 반전(왜곡 철회) 신호라서 직전 왜곡과 병합하면
# 오염된다 (리뷰에서 배포 모델 실측: 병합 시 앵커 라벨 0.94 재오염 확인).
_GENERIC = {"그냥", "그런", "그렇", "그래", "그랬", "그게", "그거", "이번", "저번", "매번",
            "항상", "계속", "요즘", "자꾸", "어제", "오늘", "내일", "진짜", "정말", "너무",
            "많이", "조금", "사실", "생각", "느낌", "기분", "얘기", "말로", "하기", "비슷",
            "몰라", "모르", "글쎄", "별거", "아니", "달라", "때문", "그때", "지금", "여기",
            "저기", "하나", "전부", "모두", "어렵", "힘들", "그러", "이유", "소용",
            "일이", "딱히", "별로", "그다지", "워낙", "아마", "이제", "아직", "벌써", "다시",
            "결국", "원래", "어디", "뭐가", "뭔가", "잘못",
            "네네", "네에", "응응", "맞아", "맞죠", "됐어", "했어", "있었", "있어", "없었",
            "없어", "같았", "같아", "탓"}

# 꼬리는 긴 것부터 시도해야 한다 ("보다"를 "다"보다 먼저) — 튜플 나열 순서에
# 의존하지 않도록 길이 내림차순으로 정렬해 둔다.
_TAILS = tuple(sorted(_ENDINGS + _PARTICLES, key=len, reverse=True))


def content_tokens(text: str) -> set[str]:
    """발화에서 '내용어 후보'를 뽑는다: 공백 분리 → 문장부호·자모 제거 → 꼬리 제거 → 2글자+.

    꼬리 제거 규칙 (리뷰에서 잡힌 함정 반영):
    - 어근이 2글자 이상 남을 때만 뗀다. "바다·포도·사과" 같은 2글자 명사가 한 글자
      꼬리('다·가·과')에 잘려 소멸하면 화제 전환을 놓쳐 병합 오염으로 이어진다.
    - 어근이 1글자가 될 긴 어미("갔어요"→"갔")는 떼지 않고 원형을 유지한다 — 원형이
      남으면 최악이 "화제 전환 과잉 판정"(병합 포기)인데, 이는 오염보다 안전한
      방향이다. 흔한 동사 원형은 _GENERIC 의 어간("있었","했어","됐어")이 흡수한다.
    - 'ㅇㅇ·ㅠㅠ' 같은 자모 이모티콘은 내용어가 아니다 (자모 제거).
    """
    out: set[str] = set()
    for raw in re.split(r"\s+", (text or "").strip()):
        w = re.sub(r"[^\w가-힣]", "", raw)
        w = re.sub(r"[ㄱ-ㅎㅏ-ㅣ]", "", w)       # 자모 단독(ㅇㅇ, ㅠㅠ 등) 제거
        for _ in range(2):                       # "사과했어요"→(했어요)→"사과" 처럼 최대 2회
            for tail in _TAILS:                  # 긴 꼬리부터
                if w.endswith(tail):
                    stem = w[: -len(tail)]
                    if len(stem) >= 2:
                        w = stem                 # 어근이 살아남을 때만 뗀다
                    break                        # 같은 패스에서 더 짧은 꼬리 재시도 없음
        if len(w) >= 2:
            out.add(w)
    return out


# 지시 조응 마커: "그 생각만 하면…"처럼 직전 담화를 직접 가리키는 표현.
# 이게 있으면 발화가 새 명사를 데려와도(예: '잠') 이어말하기가 확정적이다 —
# e2e 검증에서 병합의 대표 수혜 사례("그 생각만 하면 잠이 안 와요")가
# 주변 명사에 걸려 기각되는 것을 잡은 보완 규칙 (담화론의 givenness 마커).
_ANAPHORA = re.compile(r"(그|이)\s?(생각|일|말|얘기|기분|느낌|상황|문제|때)")


def novelty(current: str, context_texts: list[str]) -> list[str]:
    """직전 맥락에 뿌리가 없는 '새 내용어' 목록. 비어 있으면 이어말하기로 본다.

    부분일치(앞 2글자 겹침 포함)면 기존 어휘로 간주 — "발표가/발표를" 같은
    조사 변형이나 합성어 차이를 같은 화제로 묶기 위한 근사 규칙.
    지시 조응("그 생각/그 일…")이 보이면 새 명사와 무관하게 이어말하기로 본다.
    """
    if _ANAPHORA.search(current or ""):
        return []
    ctx: set[str] = set()
    for t in context_texts:
        ctx |= content_tokens(t)
    novel = []
    for w in content_tokens(current):
        if any(w in g or g in w for g in _GENERIC):
            continue
        if any(w[:2] == c[:2] or w in c or c in w for c in ctx):
            continue
        novel.append(w)
    return novel


def merge_candidate(context_texts: list[str], current: str,
                    max_turns: int, max_chars: int) -> tuple[str, list[str]]:
    """직전 발화들 + 현재 발화를 공백으로 이어 재분류용 병합문을 만든다.

    반환: (병합문, 실제로 포함된 맥락 목록). novelty 게이트는 반드시 "포함된 맥락"
    기준으로 판정해야 한다 — 길이 초과로 앵커가 잘려 나갔는데 게이트만 전체 창을
    보면, 판정 근거와 병합 내용이 어긋난다 (리뷰 지적).

    규칙 (분류 모델 보호가 목적):
    - 특수 구분자 없이 공백 연결 — 모델이 학습 때 본 적 없는 포맷을 만들지 않는다.
    - max_turns <= 0 이면 병합하지 않는다 (파이썬 list[-0:] 이 "전체"가 되는 함정 차단
      — 0 은 "맥락 안 씀"으로 해석한다).
    - max_chars 초과 시 오래된 맥락부터 통째로 삭제. 마지막 맥락 1개도 넘치면
      그 맥락의 머리를 잘라 꼬리(현재 발화와 가까운 쪽)만 남긴다.
    - 현재 발화는 절대 자르지 않는다 (이번 턴의 신호가 최우선).
    - 상한 180자 기본값은 분류기 절단선(160토큰 ≈ 한국어 약 300자)의 안전 마진.
    """
    if max_turns <= 0:
        return current, []
    ctx = [t for t in context_texts if (t or "").strip()][-max_turns:]
    while ctx and len(" ".join(ctx) + " " + current) > max_chars:
        if len(ctx) > 1:
            ctx.pop(0)
        else:
            room = max_chars - len(current) - 1
            ctx[0] = ctx[0][-room:] if room > 10 else ""
            if not ctx[0]:
                ctx = []
            break
    if not ctx:
        return current, []
    return (" ".join(ctx) + " " + current).strip(), list(ctx)


def merge_text(context_texts: list[str], current: str, max_turns: int, max_chars: int) -> str:
    """merge_candidate 의 병합문만 필요한 곳(테스트 등)을 위한 축약형."""
    return merge_candidate(context_texts, current, max_turns, max_chars)[0]


